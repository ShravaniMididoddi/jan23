package Demo;

public class DoWhile {

	public static void main(String[] args) {
		int i =5;
		do {
			System.out.println(i);
			i=i+1; //i++ Increment
		}while(i<=9);
		int j=2;
		do {
			System.out.println(j+i);
			j++;
		}while(j<5);{
			System.out.println("program ends");
		}
	}

}
